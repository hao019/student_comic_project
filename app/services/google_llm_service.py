import json
import os
import time

from dotenv import load_dotenv
from google import genai

from app.schemas import NewsComicPageScript, SD35ComicPageScript


load_dotenv()

API_KEY = os.getenv("GOOGLE_API_KEY") or os.getenv("GEMINI_API_KEY")
client = genai.Client(api_key=API_KEY)

PRIMARY_TEXT_MODEL = os.getenv("GEMINI_TEXT_MODEL", "gemini-3.1-pro-preview")
TEXT_MODELS = list(dict.fromkeys([
    PRIMARY_TEXT_MODEL,
    "gemini-3.1-flash-lite",
]))

MAX_RETRIES_PER_MODEL = 3


def clean_json(text: str) -> str:
    text = text.strip()

    if text.startswith("```json"):
        text = text.replace("```json", "", 1).strip()
    if text.startswith("```"):
        text = text.replace("```", "", 1).strip()
    if text.endswith("```"):
        text = text[:-3].strip()

    start = text.find("{")
    end = text.rfind("}")
    if start != -1 and end != -1 and end > start:
        text = text[start:end + 1]

    return text


def generate_json(prompt: str) -> dict:
    last_error = None

    for model_name in TEXT_MODELS:
        for retry in range(MAX_RETRIES_PER_MODEL):
            try:
                response = client.models.generate_content(
                    model=model_name,
                    contents=prompt,
                )
                return json.loads(clean_json(response.text or ""))
            except Exception as e:
                print(f"Gemini text generation failed: {model_name}")
                print(e)
                last_error = e
                time.sleep(2 + retry)

    raise RuntimeError(f"Gemini text generation failed after fallback models: {last_error}")


